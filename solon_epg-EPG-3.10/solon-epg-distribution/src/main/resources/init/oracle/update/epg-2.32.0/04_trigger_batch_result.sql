CREATE OR REPLACE TRIGGER "SW_UPDATE_ERR_RESULT_BATCH"
  AFTER INSERT ON BATCH_RESULT
  FOR EACH ROW
   WHEN (new.TEXT = 'Exécution du lanceur général') 
  BEGIN  
	  UPDATE BATCH_LOG SET ERROR_COUNT = (SELECT SUM(ERROR_COUNT) FROM BATCH_LOG WHERE PARENT_ID = :new.ID_LOG) WHERE ID_LOG = :new.ID_LOG;
END;
/
ALTER TRIGGER "SW_UPDATE_ERR_RESULT_BATCH" ENABLE;
